package graphics.jeux.NoPaintNoGame.jeu;

public class Chrono implements Runnable{
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //Creation des donnees membres//
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    private final int PAUSE=3; //temps d'attente entre 2 tours de boucle


    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //METHODES//
    /////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    @Override
    public void run(){
        while(true){
            LaunchNPNG.scene.repaint();

            try {Thread.sleep(PAUSE);}catch(InterruptedException e){}
        }
    }
}
